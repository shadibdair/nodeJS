var myKey="age";
var myValue=12;

var obj1={
    "name":"Bob",
     myKey:myValue
};

console.log(obj1);  //--> { name: 'Bob', myKey: 12 }


var obj2={
    "name":"Bob",
    [myKey]:myValue
};

console.log(obj2);  //--> { name: 'Bob', age: 12 }



